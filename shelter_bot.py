async def report_location(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    """Получаем геолокацию нового убежища."""
    if not update.message.location:
        await update.message.reply_text(t(ctx, "report_start"))
        return REPORT_LOCATION

    ctx.user_data["report_lat"] = update.message.location.latitude
    ctx.user_data["report_lon"] = update.message.location.longitude
    await update.message.reply_text(t(ctx, "report_desc"))
    return REPORT_DESC


async def report_desc(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    """Получаем описание/фото убежища и сохраняем."""
    lat = ctx.user_data.get("report_lat")
    lon = ctx.user_data.get("report_lon")
    if not lat or not lon:
        await update.message.reply_text(t(ctx, "report_cancel"))
        return ConversationHandler.END

    description = ""
    photo_id = ""

    if update.message.photo:
        photo_id = update.message.photo[-1].file_id
        description = (update.message.caption or "").strip()
    elif update.message.text:
        if update.message.text.strip() == "/skip":
            description = ""
        else:
            description = update.message.text.strip()

    try:
        user = update.effective_user
        await save_user_shelter(lat, lon, description, photo_id, user.id, user.username or "")
        await update.message.reply_text(t(ctx, "report_saved"))
    except Exception as e:
        logger.error("Report save error: %s", e)
        await update.message.reply_text(t(ctx, "error", err=e))

    return ConversationHandler.END


async def report_cancel(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(t(ctx, "report_cancel"))
    return ConversationHandler.END




def main():
    if BOT_TOKEN == "YOUR_TOKEN_HERE":
        print("❌ Set BOT_TOKEN"); return
    if not DATABASE_URL:
        print("❌ Set DATABASE_URL"); return

    import asyncio
    try:
        asyncio.get_event_loop().run_until_complete(db_init())
    except Exception as e:
        logger.error("DB init failed: %s", e)

    _load_miklat_data()
    app = Application.builder().token(BOT_TOKEN).build()

    review_conv = ConversationHandler(
        entry_points=[CallbackQueryHandler(cb_review_start, pattern=r"^review:")],
        states={
            REVIEW_TEXT:  [MessageHandler(filters.TEXT, review_text)],
            REVIEW_PHOTO: [MessageHandler(filters.PHOTO | filters.TEXT, review_photo)],
        },
        fallbacks=[CommandHandler("cancel", review_cancel)],
        per_message=False,
    )

    # Фильтр для кнопки «Добавить миклат / הוסף מקלט / Add shelter»
    _report_btn_texts = [TEXTS[l].get("menu_report", "") for l in TEXTS]
    report_btn_filter = filters.Text(_report_btn_texts)

    report_conv = ConversationHandler(
        entry_points=[
            CommandHandler("report", cmd_report),
            MessageHandler(report_btn_filter, cmd_report),
        ],
        states={
            REPORT_LOCATION: [MessageHandler(filters.LOCATION, report_location)],
            REPORT_DESC:     [MessageHandler(filters.PHOTO | filters.TEXT, report_desc)],
        },
        fallbacks=[CommandHandler("cancel", report_cancel)],
        per_message=False,
    )

    app.add_handler(report_conv)
    app.add_handler(CommandHandler("start",  start))
    app.add_handler(CommandHandler("ping",   cmd_ping))
    app.add_handler(CommandHandler("status", cmd_status))
    app.add_handler(CommandHandler("lang",   cmd_lang))
    app.add_handler(review_conv)
    app.add_handler(CallbackQueryHandler(cb_lang,           pattern=r"^lang:"))
    app.add_handler(CallbackQueryHandler(cb_select_shelter, pattern=r"^select:"))
    app.add_handler(CallbackQueryHandler(cb_back,           pattern=r"^back$"))
    app.add_handler(CallbackQueryHandler(cb_checkin,        pattern=r"^checkin:"))
    app.add_handler(CallbackQueryHandler(cb_checkout,       pattern=r"^checkout$"))
    app.add_handler(MessageHandler(filters.LOCATION, handle_location))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_text))
    app.add_error_handler(global_error_handler)

    print("🚀 ялла, миклат! (nationwide) запущен.")
    app.run_polling(drop_pending_updates=True)


if __name__ == "__main__":
    main()
