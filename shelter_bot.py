        muni_ext = [s for s in shelters_muni if s["distance"] <= expanded_r]
        waze_ext = [s for s in shelters_waze if s["distance"] <= expanded_r]
        kml_ext  = [s for s in shelters_kml  if s["distance"] <= expanded_r]
